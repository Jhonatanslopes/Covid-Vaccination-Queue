    private void listarDadosFila() {
        JLabel proximoDaDila = jLabelProximoFila;
        JLabel proximoIdDaFila = jLabelId;
        int contador = 0;
        Object indice;
        Object id;
        try {
            DAO dao = new DAO();

            DefaultTableModel model = (DefaultTableModel) jTableFilaVacincacao.getModel();
            model.setNumRows(0);

            ArrayList<FilaVacincao> fila = dao.visualizarFila();

            for (int num = 0; num < fila.size(); num++) {
                model.addRow(new Object[]{
                    fila.get(num).getId(),
                    fila.get(num).getNome(),
                    fila.get(num).getOrdemFila(),
                    fila.get(num).getOrdem()
                });
              }
            indice = " " + fila.get(0).getNome();
            id = fila.get(0).getId();
            String idToString = Integer.toString((int) id);
            proximoDaDila.setText((String) indice);
            proximoIdDaFila.setText(idToString);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Não há pessoas na fila para vacinação.");
                jLabelId.setText("");
                jLabelProximoFila.setText("");
                jButton1.setEnabled(false);
        }    
    }
