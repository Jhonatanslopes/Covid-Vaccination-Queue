    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int idFila = Integer.parseInt(jLabelId.getText()); // Obtendo o proximo ID da fila e passando para INT
        String nome = jLabelProximoFila.getText(); // Obtendo o proximo nome da fila
       
       try {
           int escolha = JOptionPane.showConfirmDialog(null,
           "Confirmar vacina de " + nome + "?", "Confirmação", JOptionPane.YES_NO_OPTION); // Opção de confirm
           if (escolha == JOptionPane.YES_OPTION) {
                Date dataAtual = new Date(); //Usando a classe Date
                String data = new SimpleDateFormat("dd/MM/yyyy").format(dataAtual); // Pegando a data atual
                String dia = data.substring(0, 2); // Pegando o dia
                String mes = data.substring(3, 5); // Pegando o mês
                String ano = data.substring(6); // Pegando o ano
                String DataParaMySql = ano+"-"+mes+"-"+dia; // Ajustando para o padrão MySQL
                Vacinacao cadastroData = new Vacinacao(DataParaMySql, idFila);
                DAO dao = new DAO();
                dao.preencherData(cadastroData); // Inserindo data convertida
            } 
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao confirmar vacinação");
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

